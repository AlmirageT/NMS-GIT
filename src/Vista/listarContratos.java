    private void btnFinalizarContratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarContratoActionPerformed
        int fila = tContrato.getSelectedRow();
        if (fila != -1) {
            Object codigo = tContrato.getValueAt(fila, 0);
            String cod = String.valueOf(codigo);
            int codigoC = Integer.parseInt(cod);
            try {
                contrato = controller.findContratos(BigDecimal.valueOf(codigoC));
                contrato.getIdContrato();
                contrato.getFechaInicio();
                contrato.getFechaTermino();
                contrato.getCreado();
                contrato.setModificado(fecha);
                contrato.getMonto();
                contrato.getRutCliente();
                contrato.getRazonSocial();
                contrato.setIdContratoEstadoFk(ce2);
                contrato.getPagosIdPago();
                controller.edit(contrato);
                JOptionPane.showMessageDialog(this, "Se ha Finalizado el Contrato", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
                cargarTabla();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Validación", 0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Favor seleccionar la fila que desea finalizar", "Validación", 0);
        }
    }//GEN-LAST:event_btnFinalizarContratoActionPerformed

