        private void cargarTabla() {
        cargarColumna();
        for (Contratos u : controller.findContratosEntities()) {
            String estado = "";
            Date fechaInicio = u.getFechaInicio();
            Date fechaTermino = u.getFechaTermino();
            Date fechaCreado = u.getCreado();
            Date fechaModificado = u.getModificado();
            SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");
            String fechaTexto = form.format(fechaInicio);
            String ft = form.format(fechaTermino);
            String fc = form.format(fechaCreado);
            String fm = form.format(fechaModificado);

            if (u.getIdContratoEstadoFk().equals(ce)) {
                estado = "En curso";
            } else if (u.getIdContratoEstadoFk().equals(ce2)) {
                estado = "Finalizado";
            } else if (u.getIdContratoEstadoFk().equals(ce3)) {
                estado = "Desactivado";
            }
            modelo.addRow(new Object[]{u.getIdContrato(), u.getRutCliente(), fechaTexto, ft, fc, fm, u.getMonto(), estado, u.getRazonSocial()});
        }
    }

    private void cargarTablarPorCodigo(String rutCliente) {
        cargarColumna();
        if (!rutCliente.isEmpty() && !rutCliente.trim().equals("")) {
        for (Contratos u : controller.buscarContrato(rutCliente)) {
            String estado = "";
            Date fechaInicio = u.getFechaInicio();
            Date fechaTermino = u.getFechaTermino();
            Date fechaCreado = u.getCreado();
            Date fechaModificado = u.getModificado();
            SimpleDateFormat form = new SimpleDateFormat("dd/MM/yyyy");
            String fechaTexto = form.format(fechaInicio);
            String ft = form.format(fechaTermino);
            String fc = form.format(fechaCreado);
            String fm = form.format(fechaModificado);

            if (u.getIdContratoEstadoFk().equals(ce)) {
                estado = "En curso";
            } else if (u.getIdContratoEstadoFk().equals(ce2)) {
                estado = "Finalizado";
            } else if (u.getIdContratoEstadoFk().equals(ce3)) {
                estado = "Desactivado";
            }
            modelo.addRow(new Object[]{u.getIdContrato(), u.getRutCliente(), fechaTexto, ft, fc, fm, u.getMonto(), estado, u.getRazonSocial()});
            
        }
        }else{
            JOptionPane.showMessageDialog(this, "No se encontro el rut ingresado en la base de datos.");
            cargarTabla();
            limpiar();
        }

    }
    private void cargarColumna() {
        modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Rut Cliente");
        modelo.addColumn("Fecha Inicio");
        modelo.addColumn("Fecha Termino");
        modelo.addColumn("Creado");
        modelo.addColumn("Modificado");
        modelo.addColumn("Monto");
        modelo.addColumn("Estado Contrato");
        modelo.addColumn("Razon Social");
        tContrato.setModel(modelo);
    }
